    public void cargarCelebracionesEnTabla(Iterator<Celebracion> iterador) {
        DefaultTableModel modelo = (DefaultTableModel) tbl_Celebraciones.getModel();
        modelo.setRowCount(0); // Limpiar tabla

        // Formateador para mostrar solo la fecha
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

        while (iterador.hasNext()) {
            Celebracion c = iterador.next();
            modelo.addRow(new Object[]{
                c.getId(),
                sdf.format(c.getFecha()), 
                c.getDescripcion(),
                c.getPais()
            });
        }
    }

    
